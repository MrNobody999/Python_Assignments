# What is the boolean data type in Python?
# List all values that are evaluates to False.

# Answer:
# In Python, the boolean data type is represented by the built-in type 'bool'. It has two possible values: True and False. These values are used to represent truth values in logical expressions and conditional statements.
# Several values in Python evaluate to False when converted to a boolean context. These include:
# 1. The boolean value False itself.  
# 2. None, which represents the absence of a value.
# 3. Numeric zero values: 0 (integer), 0.0 (float), and 0j (complex number).
# 4. Empty sequences and collections: '', (), [], {}, set(), and range(0).
# 5. Objects of user-defined classes that implement a __bool__() or __len__() method that returns False or 0, respectively.

