# What is set in Python?

# Answer:
# In Python, a set is an unordered collection of unique elements. Sets are mutable, meaning that you can add or remove elements after the set has been created. However, the elements themselves must be immutable (e.g., numbers, strings, tuples).
# Sets are defined using curly braces {} or the set() constructor. They are commonly used for operations that involve membership testing, removing duplicates from a sequence, and performing mathematical set operations like union, intersection, difference, and symmetric difference.
# Example of creating a set:
my_set = {1, 2, 3, 4}

