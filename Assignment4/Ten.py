# What is the difference between:
range(1,10)
range(1,10,2)
# Explain the parameters.

# The first parameter (1) is the starting value (inclusive), the second parameter (10) is the ending value (exclusive) and the third parameter (2) is the step value, which determines the increment between each number in the sequence.  
# So, range(1,10) generates numbers from 1 to 9 with a default step of 1, while range(1,10,2) generates numbers from 1 to 9 with a step of 2, resulting in odd numbers only: 1, 3, 5, 7, 9. 
