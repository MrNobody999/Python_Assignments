#Predict the output:
a = 5
print(type(a))
a = 5.5
print(type(a))
a = "Python"
print(type(a))
#What Python feature does this demonstrate?
# 
# Ans: The output of the code will be:
# <class 'int'>
# <class 'float'>
# <class 'str'>
# This demonstrates Python's dynamic typing feature, which allows variables to change their data type based on the value assigned to them. In Python, you do not need to declare a variable's type explicitly; the interpreter infers the type at runtime based on the assigned value.   