# Explain why the following code works without declation:
x = 100
#Compare this with C or Java.
#Ans: In Python, variables are dynamically typed, which means you do not need to declare a variable's type before using it. When you assign a value to a variable, Python automatically determines the variable's type based on the value assigned. This is different from languages like C or Java, where you must explicitly declare the type of a variable before using it. In C or Java, you would need to specify the type (e.g., int, float, String) when declaring a variable, whereas in Python, you can simply assign a value to a variable, and Python takes care of the rest.
print(x)    
# The code works without declaration because Python is a dynamically typed language.

