#What is the difference between:
# Function with parameters
# Function without parameters
# Give one example of each.

#Ans: A function with parameters is a function that takes inputs (arguments) to perform its task, while a function without parameters does not take any inputs. 

# Example of a function with parameters:
def add_numbers(a, b):
    return a + b

# Example of a function without parameters:
def print_hello():
    print("Hello, World!")
