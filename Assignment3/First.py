# Write a function to accept two numbers as parameters and return their multiplication.

#Ans: User-defined functions are functions that are created by the user to perform specific tasks. They allow for code reusability and better organization of code. 
def multiply_numbers(num1, num2):
    return num1 * num2

result = multiply_numbers(5, 10)
print("The multiplication of the two numbers is:", result)
