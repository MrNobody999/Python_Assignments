# What id difference between:
x = 10
x = "Ten"

#is this allowed ? Why?

# Ans: Yes, this is allowed in Python because it is a dynamically typed language. In Python, variables do not have a fixed data type, and you can reassign a variable to a value of a different type at any time. In the example above, the variable 'x' is first assigned an integer value of 10, and then it is reassigned to a string value of "Ten". Python automatically handles the type changes without any issues. This flexibility is one of the key features of dynamically typed languages like Python.

