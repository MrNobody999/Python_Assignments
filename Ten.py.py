# print("10" + "20")
# print(10 + 20)
# Explain the difference.

print("10" + "20")
print(10 + 20)
# Output:
# 1020
# 30
# Explanation:
# In the first print statement, "10" and "20" are string literals. The + operator concatenates the two strings, resulting in "1020".
# In the second print statement, 10 and 20 are integers. The + operator performs arithmetic addition, resulting in the integer 30.  

